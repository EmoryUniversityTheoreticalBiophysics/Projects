function [color_, symbol_] = getLCP_colorsAndSymbol(monkey, site)
%
% color is 2x1 cell (i.e., 2 colors: saturated, polyunsaturated)
% symbol is char

% colors are based on site


colors = { ...
    'LC',  [241, 88, 84]; ...
    'IC',  [96, 189, 104]; ...
    'SC',  [93, 165, 218]; ...
    'MT',  'k'; ...
    'ACC', [178, 118, 178]; ...
    'PCC', [178, 145, 47]};
sat    = [3.0 0.5]; % saturations
Lsite  = strcmp(site, colors(:,1));
color_ = {[], []};
for cc = 1:2
    col = rgb2hsv(colors{Lsite,2}./255);
    col(2) = min(1, col(2).*sat(cc));    
    color_{cc} = hsv2rgb(col);
end

